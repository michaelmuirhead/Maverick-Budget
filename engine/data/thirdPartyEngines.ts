import type { GameEngine, LicenseTerms, TechBranch } from "../types/engine";

// Seed data for third-party engines. These aren't built by any in-game studio —
// they're represented as licensable options that emerge on schedule.
// When game year >= emergedYear, the engine becomes available to all studios.
// When discontinuedYear is reached, it stops accepting new licensees.

export interface ThirdPartyEngineSeed {
  id: string;                  // stable identifier
  name: string;
  shortName?: string;
  lineageId: string;           // shared across major versions
  versionNumber: number;
  featureIds: string[];        // engine features derived from tech nodes
  branchTiers: Record<TechBranch, number>;
  overallTier: number;
  emergedYear: number;         // when studios can start licensing this
  discontinuedYear: number;    // last year new licensees accepted
  peakYear: number;            // when this engine is at its most dominant
  licenseTerms: LicenseTerms;
  initialReputation: number;   // 0-100 — how respected the engine is
  // Preferred genres — engine is tuned for these; mild bonus when used for them
  preferredGenreIds: string[];
}

