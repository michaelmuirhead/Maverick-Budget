export const THIRD_PARTY_ENGINE_BY_ID: Record<string, ThirdPartyEngineSeed> = Object.fromEntries(
  THIRD_PARTY_ENGINES.map((e) => [e.id, e])
);

// Helper: engines that are available to license in a given year
export function thirdPartyEnginesAvailableInYear(year: number): ThirdPartyEngineSeed[] {
  return THIRD_PARTY_ENGINES.filter(
    (e) => year >= e.emergedYear && year < e.discontinuedYear
  );
}

// Hydrate a seed into a full GameEngine record (for initial state)
export function hydrateThirdPartyEngine(seed: ThirdPartyEngineSeed, currentYear: number): GameEngine {
  // Currentness decays after peak year
  const yearsPastPeak = Math.max(0, currentYear - seed.peakYear);
  const currentness = Math.max(20, 100 - yearsPastPeak * 10);

  return {
    id: seed.id,
    name: seed.name,
    shortName: seed.shortName,
    lineageId: seed.lineageId,
    versionNumber: seed.versionNumber,
    origin: "third_party",
    ownerStudioId: null,
    status: currentYear >= seed.discontinuedYear ? "discontinued" : "public_release",
    featureIds: seed.featureIds,
    branchTiers: seed.branchTiers,
    overallTier: seed.overallTier,
    buildCost: 0,
    monthlyMaintenanceCost: 0,
    licenseTerms: seed.licenseTerms,
    startedYear: seed.emergedYear,
    releasedYear: seed.emergedYear,
    currentness,
    engineReputation: seed.initialReputation,
    projectsBuilt: 0,
    totalLicensees: 0,
  };
}
