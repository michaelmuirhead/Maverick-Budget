export const OUTLET_BY_ID: Record<string, ReviewOutlet> = Object.fromEntries(
  REVIEW_OUTLETS.map((o) => [o.id, o])
);

// Helper: outlets active in a given year
export function outletsActiveInYear(year: number): ReviewOutlet[] {
  return REVIEW_OUTLETS.filter((o) => {
    const started = year >= o.activeFromYear;
    const stillActive = !o.activeToYear || year <= o.activeToYear;
    return started && stillActive;
  });
}
